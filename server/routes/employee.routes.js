// Add new employee
router.post('/add', async (req, res) => {
  const { first_name, last_name, job_title, gender, phone, ssn, hire_date } = req.body;
  const sql = `INSERT INTO employee(first_name, last_name, job_title, gender, phone, ssn, hire_date )
              VALUES (?,?,?,?,?,?,?)`;
  db.query(sql, [first_name, last_name, job_title, gender, phone, ssn, hire_date], (err, result) => {
    if (err) {
      return res.status(500).json({
        message: 'Error adding employee',
        error: err.message
      })
    }

    // Fetch the newly created employee's email and password (generated by trigger)
    const employeeId = result.insertId;
    const fetchSql = `SELECT email, password FROM employee WHERE employee_id = ?`;
    db.query(fetchSql, [employeeId], (fetchErr, fetchResult) => {
      if (fetchErr) {
        return res.status(500).json({
          message: 'Employee added but failed to retrieve credentials',
          error: fetchErr.message
        });
      }

      const employeeData = fetchResult[0];
      res.status(201).json({
        message: 'Employee added successfully',
        employeeId: employeeId,
        email: employeeData.email,
        temporaryPassword: employeeData.password
      });
    });
  })
});

// Update an employee
router.put('/:id', async (req, res) => {
  const {
    first_name, last_name, job_title, gender,
    email, phone, ssn, hire_date, terminate_date
  } = req.body;
  const id = req.params.id;

  // Convert dates to YYYY-MM-DD
  const hireDate = hire_date ? hire_date.slice(0, 10) : null;
  const terminateDate = terminate_date ? terminate_date.slice(0, 10) : null;

  const sql = `
    UPDATE employee
    SET
      first_name = ?,
      last_name = ?,
      job_title = ?,
      gender = ?,
      email = ?,
      phone = ?,
      ssn = ?,
      hire_date = ?,
      terminate_date = ?
    WHERE employee_id = ?;
  `;
  db.query(sql, [
    first_name, last_name, job_title, gender,
    email, phone, ssn, hireDate, terminateDate, id
  ], (err, result) => {
    if (err) {
      console.error("DB error:", err);
      return res.status(500).json({
        message: 'Error updating employee',
        error: err.message
      });
    }
    res.json({ message: "Employee updated successfully", data: result });
  });
});

